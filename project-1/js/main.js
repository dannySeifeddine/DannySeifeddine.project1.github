var email = document.forms['form']['email']
var password = document.forms['form']['password']
var password2 = document.forms['form']['password2']

var email_error = document.getElementById('error')
var password_error = document.getElementById('error-password')
var password2_error = document.getElementById('error-password2')


function validate() {
    
    if (email.value.length < 9) {
        
        email_error.style.display = "block";
        return false

    }

    if (password.value.length < 9) {
        
        password_error.style.display = "block";
        return false
        
    }


    if (password2.value.length < 9) {
        
        password.value.length = password2.value.length;
        password2_error.style.display = "block";
        return false
        
    }

}



